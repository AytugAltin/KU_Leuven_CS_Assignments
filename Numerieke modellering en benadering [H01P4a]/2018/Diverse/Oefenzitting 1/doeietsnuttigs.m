function [uitvoer1, uitvoer2] = doeietsnuttigs(invoer1, invoer2, invoer3)
% DOEIETSNUTTIGS
% Deze functie berekent de som van drie inputs en het product van dezelfde
% drie inputs.
% Inputs: 
%   invoer1 Eerste getal
%   invoer2 Tweede getal
%   invoer3 Derde getal%
% Outputs: 
%   uitvoer1 Som van de drie getallen
%   uitvoer2 Product van de drie getallen

% Auteur: Michiel Vandecappelle
% Versie 1.0 20/02/2018


% Som
uitvoer1 = invoer1+invoer2+invoer3;

% Product  
uitvoer2 = invoer1*invoer2*invoer3;

end